-- ==========================================
-- 2. CATÁLOGOS BASE (Nivel 0)
-- ==========================================
CREATE TABLE area (
    cve_area INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cve_area_padre INT NULL,
    abreviatura VARCHAR(5) NOT NULL,
    nombre VARCHAR(60) NOT NULL,
    activo BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE tipo_profesor (
    cve_tipo_profesor INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(30) NOT NULL,
    descripcion TEXT
);

CREATE TABLE roles (
    cve_rol INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL UNIQUE,
    descripcion TEXT,
    fecha_alta TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);



CREATE TABLE persona (
    cve_persona INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL,
    apellido_paterno VARCHAR(35),
    apellido_materno VARCHAR(35),
    cve_carrera INT,
    cve_nivel_estudio INT,
    fecha_alta TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE edificio (
    cve_edificio INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(80),
    abreviatura VARCHAR(5),
    activo BOOLEAN DEFAULT TRUE
);

CREATE TABLE tipo_aula (
    cve_tipo_aula INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(45) NOT NULL
);

CREATE TABLE tipos_bien (
    cve_tipo INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    clave VARCHAR(2) NOT NULL,
    nombre VARCHAR(100) NOT NULL
);

CREATE TABLE marcas (
    cve_marca INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_marca VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE motivo_de_movimiento (
    cve_motivo INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_motivo VARCHAR(50) NOT NULL,
    requiere_aprobacion BOOLEAN DEFAULT FALSE
);

-- ==========================================
-- 3. TABLAS CON REFERENCIAS (Nivel 1)
-- ==========================================
CREATE TABLE profesor (
    cve_profesor INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cve_persona INT NOT NULL REFERENCES persona(cve_persona),
    cve_tipo_profesor INT NOT NULL REFERENCES tipo_profesor(cve_tipo_profesor),
    cve_area INT REFERENCES area(cve_area),
    activo BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE alumnos (
    cve_alumno INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cve_persona INT NOT NULL REFERENCES persona(cve_persona),
    matricula VARCHAR(15) NOT NULL,
    fecha_inscrito DATE NOT NULL,
    inscrito BOOLEAN NOT NULL,
    activo BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE usuarios (
    cve_usuario INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario VARCHAR(50) UNIQUE NOT NULL,
    contrasena_hash VARCHAR(255) NOT NULL,
    cve_rol INT REFERENCES roles(cve_rol),
    nombre VARCHAR(100),
    email VARCHAR(255),
    activo BOOLEAN DEFAULT true,
    cve_persona INT REFERENCES persona(cve_persona),
    fecha_registro TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    fecha_actualizacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);


CREATE TABLE aula (
    cve_aula INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cve_edificio INT NOT NULL REFERENCES edificio(cve_edificio),
    cve_tipo_aula INT NOT NULL REFERENCES tipo_aula(cve_tipo_aula),
    cve_profesor INT REFERENCES profesor(cve_profesor),
    nombre VARCHAR(30) NOT NULL,
    codigo_barras varchar(50) unique, -- nuevo campo y único
    codigo_qr varchar(255) unique,    -- ahora es único
    capacidad SMALLINT
);

CREATE TABLE familias_articulos (
    cve_familia INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    clave VARCHAR(2) NOT NULL,
    nombre VARCHAR(100) NOT NULL,
    cve_tipo INT REFERENCES tipos_bien(cve_tipo)
);

CREATE TABLE modelos (
    cve_modelo INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_modelo VARCHAR(100) NOT NULL,
    cve_marca INT REFERENCES marcas(cve_marca),
    descripcion TEXT
);

CREATE TABLE adscripcion (
    cve_adscripcion INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    clave_adscripcion VARCHAR(20) NOT NULL UNIQUE,
    nombre_adscripcion VARCHAR(50) NOT NULL,
    estado BOOLEAN DEFAULT TRUE,
    fecha_registro DATE DEFAULT CURRENT_DATE
);

-- ==========================================
-- 4. TABLAS CON REFERENCIAS (Nivel 2)
-- ==========================================
CREATE TABLE articulos (
    cve_articulo INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    clave VARCHAR(4) NOT NULL,
    nombre VARCHAR(100) NOT NULL,
    cve_familia INT REFERENCES familias_articulos(cve_familia)
);

CREATE TABLE adscripcion_persona (
    cve_adscripcion_persona INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cve_adscripcion INT NOT NULL REFERENCES adscripcion(cve_adscripcion),
    cve_persona INT NOT NULL REFERENCES persona(cve_persona),
    activo BOOLEAN DEFAULT TRUE,
    fecha_registro TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);


