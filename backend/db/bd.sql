-- creación de la tabla numero_resguardo
create table numero_resguardo (
    cve_resguardo_pk int generated by default as identity primary key,
    no_resguardo varchar(50) not null,
    cve_persona int not null references persona(cve_persona),
    fecha date default current_date,
    hora time default current_time,
    estado boolean default true,
    fecha_registro date default current_date

);


-- creación de la tabla bienes actualizada
create table bienes (
    cve_bien int generated by default as identity primary key,
    nombre varchar(100) not null,
    codigo_barras varchar(50) unique,
    nfc varchar(50) unique,
    codigo_qr varchar(255) unique,
    no_serie varchar(50) unique,
    no_factura varchar(50) unique,
    descripcion text,
    foto_url VARCHAR(500),
    foto_drive_id VARCHAR(255),
    cve_modelo int references modelos(cve_modelo),
    cve_articulo int references articulos(cve_articulo),
    costo_unitario decimal(10, 2),
    cve_aula int references aula(cve_aula),
    cve_encargado int references persona(cve_persona),
    estado_fisico varchar(20),        -- 'bueno', 'regular', 'malo', 'mantenimiento', 'baja'
    estado_prestamo varchar(20) default 'disponible',  -- 'disponible', 'prestado', 'reservado', 'vencido'
    activo boolean default true,
    fecha_registro date default current_date
);

