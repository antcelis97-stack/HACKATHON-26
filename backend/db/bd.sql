-- 6. OPERACIONES (Movimientos)
-- ==========================================
CREATE TABLE auditorias (
    cve_auditoria INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fecha_auditoria DATE DEFAULT CURRENT_DATE,
    cve_auditor INT REFERENCES persona(cve_persona),
    observaciones_generales TEXT
);

CREATE TABLE auditoria_detalle (
    cve_auditoria_det INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cve_auditoria INT REFERENCES auditorias(cve_auditoria),
    cve_bien INT REFERENCES bienes(cve_bien),
    encontrado BOOLEAN,
    estado_encontrado VARCHAR(50)
);

CREATE TABLE prestamos (
    cve_prestamo INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cve_bien INT NOT NULL REFERENCES bienes(cve_bien),
    cve_persona_solicita INT NOT NULL REFERENCES persona(cve_persona),
    fecha_solicitud TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    fecha_devolucion_pactada TIMESTAMP,
    fecha_devolucion_real TIMESTAMP NULL,
    estado_prestamo VARCHAR(20) DEFAULT 'pendiente',
    observaciones TEXT
);

CREATE TABLE bitacora_movimientos (
    cve_movimiento INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cve_bien INT REFERENCES bienes(cve_bien),
    cve_motivo INT REFERENCES motivo_de_movimiento(cve_motivo),
    cve_persona_accion INT REFERENCES persona(cve_persona),
    fecha_movimiento TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    observaciones TEXT
);

-- ================================================================
-- 7. TRIGGERS (Lógica Automática)
-- ================================================================

-- A. Actualizar estado del bien según el préstamo
CREATE OR REPLACE FUNCTION fn_trg_actualizar_estado_prestamo()
RETURNS TRIGGER AS $$
BEGIN
    IF (NEW.estado_prestamo = 'aprobado') THEN
        UPDATE bienes SET estado_prestamo = 'prestado' WHERE cve_bien = NEW.cve_bien;
    ELSIF (NEW.estado_prestamo = 'devuelto') THEN
        UPDATE bienes SET estado_prestamo = 'disponible' WHERE cve_bien = NEW.cve_bien;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_actualizar_estado_prestamo
AFTER UPDATE ON prestamos
FOR EACH ROW
EXECUTE FUNCTION fn_trg_actualizar_estado_prestamo();

-- B. Log automático de cambios de estado físico
CREATE OR REPLACE FUNCTION fn_trg_log_estado_fisico()
RETURNS TRIGGER AS $$
BEGIN
    INSERT INTO bitacora_movimientos (cve_bien, cve_motivo, cve_persona_accion, fecha_movimiento, observaciones)
    VALUES (NEW.cve_bien, 1, 1, CURRENT_TIMESTAMP, 'El estado físico cambió a: ' || NEW.estado_fisico);
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_log_estado_fisico
AFTER UPDATE ON bienes
FOR EACH ROW
WHEN (OLD.estado_fisico IS DISTINCT FROM NEW.estado_fisico)
EXECUTE FUNCTION fn_trg_log_estado_fisico();

